a = 10
b = 3

print(a + b)   # 13
print(a - b)   # 7
print(a * b)   # 30
print(a / b)   # 3.333...
print(a % b)   # 1  (Remainder)
print(a // b)  # 3  (Floor division, whole part only)
print(a ** b)  # 1000 (Exponent, 10^3)


x = 5
y = 10

print(x == y)   # False
print(x != y)   # True
print(x < y)    # True
print(x >= y)   # False

age = 25
is_student = True

print(age > 18 and is_student)  # True
print(age < 18 or is_student)   # True
print(not is_student)           # False

